    private void jBtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSalvarActionPerformed
        // TODO add your handling code here:
        if (jDataLanc.getDate() == null) {
            JOptionPane.showMessageDialog(rootPane, "É necessário informar a data do lançamento.");
        } else if (jResponsavel.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "É necessário informar o responsável pelo registro.");
        } else if (jComboBoxTipoRefeicao.getSelectedItem().equals("Selecione...") || jComboBoxTipoRefeicao.getSelectedItem().equals("") || jComboBoxTipoRefeicao.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(rootPane, "É necessário o tipo de Refeição.");
        } else if (jComboBoxPavilhao.getSelectedItem().equals("Selecione...") || jComboBoxPavilhao.getSelectedItem().equals("") || jComboBoxPavilhao.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(rootPane, "Informe qual é o pavilhão.");
        } else {
            objPagtoRef.setStatusRef(jStatusLanc.getText());
            objPagtoRef.setDataLanc(jDataLanc.getDate());
            objPagtoRef.setResponsavel(jResponsavel.getText());
            objPagtoRef.setHorarioInicial(jHorarioInicial.getText());
            objPagtoRef.setHoraTermino(jHorarioTermino.getText());
            objPagtoRef.setTipoRefeicao((String) jComboBoxTipoRefeicao.getSelectedItem());
            objPagtoRef.setDescricaoPav((String) jComboBoxPavilhao.getSelectedItem());
            objPagtoRef.setObservacao(jObservacao.getText());
            if (acao == 1) {
                objPagtoRef.setUsuarioInsert(nameUser);
                objPagtoRef.setDataInsert(dataModFinal);
                objPagtoRef.setHorarioInsert(horaMov);
                //
                control.incluirPagamentoKit(objPagtoRef);
                buscarCodigo();
                objLog();
                controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                Salvar();
                JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
            }
            if (acao == 2) {
                objPagtoRef.setUsuarioUp(nameUser);
                objPagtoRef.setDataUp(dataModFinal);
                objPagtoRef.setHorarioUp(horaMov);
                //
                objPagtoRef.setIdRef(Integer.valueOf(jIdLanc.getText()));
                control.alterarPagamentoKit(objPagtoRef);
                objLog();
                controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                Salvar();
                JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
            }
        }
    }//GEN-LAST:event_jBtSalvarActionPerformed

