                conecta.executaSQL("SELECT * FROM PAGAMENTO_REFEICAO "
                        + "INNER JOIN PAVILHAO "
                        + "ON PAGAMENTO_REFEICAO.IdPav=PAVILHAO.IdPav "
                        + "WHERE IdRef='" + IdLanc + "'");
